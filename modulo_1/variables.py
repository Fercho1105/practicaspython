#Aqui hay una variable de tipo entero
numero=147
print(numero) #Imprimimos la variable numero
print(type(numero))

#Redefinimos la variable "numero"
numero=147.45
print(numero)
print(type(numero))

#Variable tipo complejo
complejo=1+2j
print(complejo)
print(type(complejo))

#Variable tipo caracter
palabra="Hola"
print(palabra)
print(type(palabra))

#Variable tipo booleano
bandera=True
print(bandera)
print(type(bandera))

#Variable tipo nonetype
vacio=None
print(vacio)
print(type(vacio))

